def render_dependency_graph(bindings: List[DIBinding]) -> str:
    """Produce a Mermaid dependency graph document."""
    lines: List[str] = []
    lines.append(
        _header(
            "Dependency Graph",
            "DI binding graph showing which concrete class is registered under each "
            "interface in the AIOS Service Registry (extracted from bootstrap.py).",
        )
    )

    lines.append("## Overview\n")
    lines.append(
        f"The Composition Root registers **{len(bindings)} DI bindings**. "
        "The graph below visualises the interface → concrete mappings.\n\n"
    )

    lines.append("## DI Bindings Table\n")
    lines.append("| # | Interface | Concrete | Line |")
    lines.append("|---|-----------|----------|------|")
    for i, b in enumerate(bindings, 1):
        lines.append(f"| {i} | `{b.interface}` | `{b.concrete}` | L{b.line_number} |")
    lines.append("")

    # Mermaid graph (top-level grouping)
    lines.append("## Dependency Graph (Mermaid)\n")
    lines.append("```mermaid")
    lines.append("graph LR")
    lines.append('  subgraph "AIOS DI Bindings"')

    for b in bindings:
        iface_node = b.interface.replace(" ", "_")
        conc_node = b.concrete.replace(" ", "_")
        lines.append(f'    {iface_node}["{b.interface}"] --> {conc_node}["{b.concrete}"]')

    lines.append("  end")
    lines.append("```\n")

    return "\n".join(lines)


