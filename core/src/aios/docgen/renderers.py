# ---------------------------------------------------------------------------


def render_service_catalog(services: List[ServiceEntry]) -> str:
    """Produce a Markdown Service Catalog document."""
    lines: List[str] = []
    lines.append(
        _header(
            "Service Catalog",
            "All service interfaces registered in the Personal AI OS service registry, "
            "their implementations, and exposed API contracts.",
        )
    )
    lines.append(_toc(["Overview", "Services", "Summary Statistics"]))

    # Overview
    lines.append("## Overview\n")
    lines.append(
        f"The AIOS service layer exposes **{len(services)} service interfaces**, each "
        "paired with a concrete local or remote implementation. All services implement "
        "`ServiceLifecycle` and are wired via the Composition Root in `bootstrap.py`.\n\n"
    )

    # Services
    lines.append("## Services\n")
    for svc in services:
        lines.append(f"### {svc.name}\n")

        # Badges
        has_impl = "✅ Yes" if svc.implementation else "⚠️ No"
        lines.append("| Field | Value |")
        lines.append("|-------|-------|")
        lines.append(f"| **Module** | `{svc.module}` |")
        lines.append(f"| **File** | `{svc.file_path}` |")
        lines.append(f"| **Bases** | {', '.join(f'`{b}`' for b in svc.base_classes) or '—'} |")
        lines.append(f"| **Implementation** | `{svc.implementation or '—'}` |")
        lines.append(f"| **Impl Module** | `{svc.impl_module or '—'}` |")
        lines.append(f"| **Has Implementation** | {has_impl} |")
        lines.append("")

        if svc.docstring:
            lines.append(f"**Description:** {svc.docstring}\n")

        if svc.methods:
            lines.append("**Public Methods:**\n")
            for m in sorted(svc.methods):
                lines.append(f"- `{m}()`")
            lines.append("")

        lines.append("---\n")

    # Stats
    lines.append("## Summary Statistics\n")
    with_impl = sum(1 for s in services if s.implementation)
    lines.append("| Metric | Count |")
    lines.append("|--------|-------|")
    lines.append(f"| Total Services | {len(services)} |")
    lines.append(f"| With Implementations | {with_impl} |")
    lines.append(f"| Without Implementations | {len(services) - with_impl} |")
    lines.append("")

    return "\n".join(lines)


