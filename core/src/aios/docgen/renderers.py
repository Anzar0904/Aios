def render_provider_catalog(providers: List[ProviderEntry]) -> str:
    """Produce a Markdown Provider Catalog document."""
    lines: List[str] = []
    lines.append(
        _header(
            "Provider Catalog",
            "All registered AI model providers in the AIOS OmniRoute engine, "
            "their capabilities, costs, and routing priorities.",
        )
    )
    lines.append(_toc(["Overview", "Providers", "Cost Comparison", "Capability Matrix"]))

    lines.append("## Overview\n")
    lines.append(
        f"The OmniRoute provider layer supports **{len(providers)} AI providers**. "
        "Routing decisions are made automatically based on capability, cost, latency, "
        "and health signals.\n\n"
    )

    lines.append("## Providers\n")
    for p in sorted(providers, key=lambda x: x.priority):
        lines.append(f"### {p.name}\n")
        lines.append("| Field | Value |")
        lines.append("|-------|-------|")
        lines.append(f"| **Version** | `{p.version}` |")
        lines.append(f"| **Status** | `{p.status}` |")
        lines.append(f"| **Priority** | {p.priority} |")
        lines.append(f"| **Context Window** | {p.context_window:,} tokens |")
        lines.append(f"| **Input Cost** | ${p.cost_per_million_input:.2f} / M tokens |")
        lines.append(f"| **Output Cost** | ${p.cost_per_million_output:.2f} / M tokens |")
        lines.append(f"| **Auth Type** | `{p.auth_type}` |")
        lines.append(f"| **Is Local** | {'Yes' if p.is_local else 'No'} |")
        lines.append("")

        if p.supported_models:
            lines.append(
                f"**Supported Models:** {', '.join(f'`{m}`' for m in p.supported_models)}\n"
            )

        if p.capabilities:
            cap_bullets = [f"`{k}`: {'✅' if v else '❌'}" for k, v in p.capabilities.items()]
            lines.append("**Capabilities:**\n")
            for cap in cap_bullets:
                lines.append(f"- {cap}")
            lines.append("")

        lines.append("---\n")

    # Cost comparison table
    lines.append("## Cost Comparison\n")
    lines.append("| Provider | Context Window | Input $/M | Output $/M | Priority |")
    lines.append("|----------|---------------|-----------|------------|----------|")
    for p in sorted(providers, key=lambda x: x.cost_per_million_input):
        lines.append(
            f"| **{p.name}** | {p.context_window:,} | "
            f"${p.cost_per_million_input:.2f} | ${p.cost_per_million_output:.2f} | {p.priority} |"
        )
    lines.append("")

    # Capability matrix
    lines.append("## Capability Matrix\n")
    all_caps = sorted({cap for p in providers for cap in p.capabilities.keys()})
    header = "| Provider | " + " | ".join(all_caps) + " |"
    sep = "|---------|" + "|".join(["---"] * len(all_caps)) + "|"
    lines.append(header)
    lines.append(sep)
    for p in sorted(providers, key=lambda x: x.priority):
        row_caps = " | ".join(("✅" if p.capabilities.get(cap) else "❌") for cap in all_caps)
        lines.append(f"| **{p.name}** | {row_caps} |")
    lines.append("")

    return "\n".join(lines)


