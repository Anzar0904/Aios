def render_index(
    *,
    services_count: int,
    repositories_count: int,
    skills_count: int,
    providers_count: int,
    runtime_count: int,
    db_models_count: int,
    di_bindings_count: int,
    generated_files: List[str],
) -> str:
    """Produce a top-level index / README for docs/generated/."""
    now = datetime.datetime.utcnow().strftime(_TIMESTAMP_FMT)
    lines: List[str] = [
        _GENERATED_BANNER,
        "# AIOS Generated Documentation\n",
        f"> Auto-generated on **{now}** by `aios.docgen.engine.DocGeneratorEngine`.\n",
        "> Do not edit this directory manually — re-run the generator to update.\n\n",
        "---\n",
        "## Discovery Summary\n",
        "| Category | Discovered |",
        "|----------|------------|",
        f"| Services | {services_count} |",
        f"| Repositories | {repositories_count} |",
        f"| Skills | {skills_count} |",
        f"| Providers | {providers_count} |",
        f"| Runtime Components | {runtime_count} |",
        f"| DB Models | {db_models_count} |",
        f"| DI Bindings | {di_bindings_count} |",
        "",
        "## Generated Files\n",
    ]
    for f in sorted(generated_files):
        fname = f.split("/")[-1]
        lines.append(f"- [{fname}]({fname})")
    lines.append("")
    lines.append("## Regeneration\n")
    lines.append(
        "Run the generator from the repository root:\n\n```bash\npython -m aios.docgen\n```\n"
    )
    return "\n".join(lines)
