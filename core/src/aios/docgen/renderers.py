def render_db_model_catalog(models: List[DbModelEntry]) -> str:
    """Produce a Markdown Database Model Catalog."""
    lines: List[str] = []
    lines.append(
        _header(
            "Database Model Catalog",
            "All dataclasses, enumerations, and data models discovered in the AIOS "
            "service layer. These models represent the core domain entities.",
        )
    )

    enums = [m for m in models if m.kind == "enum"]
    dataclasses_ = [m for m in models if m.kind == "dataclass"]
    classes_ = [m for m in models if m.kind == "class"]

    lines.append(
        _toc(["Overview", "Enumerations", "Dataclasses", "Other Models", "Summary Statistics"])
    )

    lines.append("## Overview\n")
    lines.append(
        f"The domain model layer contains **{len(models)} model types**: "
        f"{len(enums)} enumerations, {len(dataclasses_)} dataclasses, and "
        f"{len(classes_)} class-based models.\n\n"
    )

    def _render_section(section_models: List[DbModelEntry], section_name: str) -> None:
        lines.append(f"## {section_name}\n")
        if not section_models:
            lines.append("_None discovered._\n")
            return
        for m in section_models:
            lines.append(f"### {m.name}\n")
            lines.append("| Field | Value |")
            lines.append("|-------|-------|")
            lines.append(f"| **Kind** | `{m.kind}` |")
            lines.append(f"| **Module** | `{m.module}` |")
            lines.append(f"| **Line** | L{m.line_number} |")
            lines.append("")

            if m.docstring:
                lines.append(f"**Description:** {m.docstring}\n")

            if m.fields:
                field_label = "Values" if m.kind == "enum" else "Fields"
                lines.append(f"**{field_label}:**\n")
                for fld in m.fields:
                    lines.append(f"- `{fld}`")
                lines.append("")

            lines.append("---\n")

    _render_section(enums, "Enumerations")
    _render_section(dataclasses_, "Dataclasses")
    _render_section(classes_, "Other Models")

    lines.append("## Summary Statistics\n")
    lines.append("| Model Type | Count |")
    lines.append("|------------|-------|")
    lines.append(f"| Enumerations | {len(enums)} |")
    lines.append(f"| Dataclasses | {len(dataclasses_)} |")
    lines.append(f"| Other Models | {len(classes_)} |")
    lines.append(f"| **Total** | **{len(models)}** |")
    lines.append("")

    return "\n".join(lines)


