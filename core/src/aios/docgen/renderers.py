def render_skill_catalog(skills: List[SkillEntry]) -> str:
    """Produce a Markdown Skill Catalog document."""
    lines: List[str] = []
    lines.append(
        _header(
            "Skill Catalog",
            "All installable skills in the Personal AI OS skills library, "
            "their commands, capabilities, and requirements.",
        )
    )
    lines.append(_toc(["Overview", "Skills", "Skill Matrix"]))

    lines.append("## Overview\n")
    lines.append(
        f"The AIOS skills system contains **{len(skills)} skills** that extend the OS "
        "capabilities with domain-specific commands and capabilities. Skills are loaded "
        "from `skill.toml` manifests.\n\n"
    )

    lines.append("## Skills\n")
    for skill in skills:
        lines.append(f"### {skill.name}\n")
        lines.append("| Field | Value |")
        lines.append("|-------|-------|")
        lines.append(f"| **ID** | `{skill.skill_id}` |")
        lines.append(f"| **Version** | `{skill.version}` |")
        lines.append(f"| **Author** | {skill.author} |")
        lines.append(f"| **Category** | {skill.category} |")
        lines.append(f"| **Has Commands** | {'✅ Yes' if skill.has_commands_py else '⚠️ No'} |")
        lines.append(f"| **TOML Path** | `{skill.toml_path}` |")
        lines.append("")

        lines.append(f"**Description:** {skill.description}\n")

        if skill.commands:
            lines.append("**Commands:**\n")
            for cmd in skill.commands:
                lines.append(f"- `{cmd}`")
            lines.append("")

        if skill.capabilities:
            lines.append(f"**Capabilities:** {', '.join(f'`{c}`' for c in skill.capabilities)}\n")

        if skill.required_models:
            lines.append(
                f"**Required Models:** {', '.join(f'`{m}`' for m in skill.required_models)}\n"
            )

        if skill.required_tools:
            lines.append(
                f"**Required Tools:** {', '.join(f'`{t}`' for t in skill.required_tools)}\n"
            )

        lines.append("---\n")

    # Skill Matrix
    lines.append("## Skill Matrix\n")
    lines.append("| Skill | Version | Category | Commands | Capabilities |")
    lines.append("|-------|---------|----------|----------|--------------|")
    for skill in skills:
        cmds = len(skill.commands)
        caps = len(skill.capabilities)
        lines.append(f"| **{skill.name}** | {skill.version} | {skill.category} | {cmds} | {caps} |")
    lines.append("")

    return "\n".join(lines)


